"""
Dataset for loading pre-generated multi-type EQ data.
Returns (wet_audio, dry_audio, params_tensor) where params_tensor
includes gain, freq, q, and filter type index per band.
"""
import os
import json
import torch
import torchaudio
from torch.utils.data import Dataset, DataLoader
from pathlib import Path

FILTER_TYPES = ["peaking", "lowshelf", "highshelf", "highpass", "lowpass"]
TYPE_TO_INDEX = {t: i for i, t in enumerate(FILTER_TYPES)}


class SyntheticEQDataset(Dataset):
    """
    Dataset to load synthetic wet/dry audio and multi-type target parameters
    generated by generate_data.py.
    """

    def __init__(self, data_dir, sample_rate=44100, duration=3.0):
        self.data_dir = Path(data_dir)
        self.sample_rate = sample_rate
        self.samples = int(sample_rate * duration)

        # Discover all target param files
        self.files = list(self.data_dir.glob("*_params.json"))

    def __len__(self):
        return len(self.files)

    def __getitem__(self, idx):
        param_path = self.files[idx]
        file_stem = str(param_path).replace("_params.json", "")

        wet_path = f"{file_stem}_wet.wav"
        dry_path = f"{file_stem}_dry.wav"

        # Load audio
        wet_waveform, sr = torchaudio.load(wet_path)
        if sr != self.sample_rate:
            wet_waveform = torchaudio.functional.resample(wet_waveform, sr, self.sample_rate)

        # Try loading dry audio (may not exist for older datasets)
        try:
            dry_waveform, _ = torchaudio.load(dry_path)
            if dry_waveform.shape[0] > 1:
                dry_waveform = torch.mean(dry_waveform, dim=0, keepdim=True)
        except Exception:
            dry_waveform = wet_waveform  # Fallback

        # Ensure single channel and exact length
        if wet_waveform.size(0) > 1:
            wet_waveform = torch.mean(wet_waveform, dim=0, keepdim=True)

        if wet_waveform.size(1) > self.samples:
            wet_waveform = wet_waveform[:, :self.samples]
        elif wet_waveform.size(1) < self.samples:
            wet_waveform = torch.nn.functional.pad(wet_waveform, (0, self.samples - wet_waveform.size(1)))

        if dry_waveform.size(1) > self.samples:
            dry_waveform = dry_waveform[:, :self.samples]
        elif dry_waveform.size(1) < self.samples:
            dry_waveform = torch.nn.functional.pad(dry_waveform, (0, self.samples - dry_waveform.size(1)))

        # Load parameters
        with open(param_path, 'r') as f:
            params = json.load(f)

        # Convert to tensor: [gain, freq, q, type_idx] per band
        target_list = []
        for band in params:
            ftype_idx = TYPE_TO_INDEX.get(band.get("type", "peaking"), 0)
            target_list.append([
                band["gain"],
                band["freq"],
                band["q"],
                float(ftype_idx)
            ])

        target_tensor = torch.tensor(target_list, dtype=torch.float32)

        # Separate components
        gain = target_tensor[:, 0]
        freq = target_tensor[:, 1]
        q = target_tensor[:, 2]
        filter_type = target_tensor[:, 3].long()

        return {
            "wet_audio": wet_waveform.squeeze(0),
            "dry_audio": dry_waveform.squeeze(0),
            "gain": gain,
            "freq": freq,
            "q": q,
            "filter_type": filter_type,
        }


def collate_fn(batch):
    """Custom collate to handle variable-length audio with padding."""
    wet_list = [item["wet_audio"] for item in batch]
    dry_list = [item["dry_audio"] for item in batch]

    max_len = max(x.shape[0] for x in wet_list)

    def pad_audio(audio, target_len):
        if audio.shape[0] < target_len:
            audio = torch.nn.functional.pad(audio, (0, target_len - audio.shape[0]))
        return audio

    wet_padded = torch.stack([pad_audio(w, max_len) for w in wet_list])
    dry_padded = torch.stack([pad_audio(d, max_len) for d in dry_list])
    gains = torch.stack([item["gain"] for item in batch])
    freqs = torch.stack([item["freq"] for item in batch])
    qs = torch.stack([item["q"] for item in batch])
    types = torch.stack([item["filter_type"] for item in batch])

    return {
        "wet_audio": wet_padded,
        "dry_audio": dry_padded,
        "gain": gains,
        "freq": freqs,
        "q": qs,
        "filter_type": types,
    }


def create_dataloaders(data_dir, batch_size=32, num_workers=4, split=(0.8, 0.1, 0.1)):
    """Create train/val/test dataloaders."""
    dataset = SyntheticEQDataset(data_dir)

    train_len = int(len(dataset) * split[0])
    val_len = int(len(dataset) * split[1])
    test_len = len(dataset) - train_len - val_len

    train_set, val_set, test_set = torch.utils.data.random_split(
        dataset, [train_len, val_len, test_len],
        generator=torch.utils.data.Generator().manual_seed(42)
    )

    train_loader = DataLoader(train_set, batch_size=batch_size, shuffle=True,
                               num_workers=num_workers, collate_fn=collate_fn)
    val_loader = DataLoader(val_set, batch_size=batch_size, shuffle=False,
                             num_workers=num_workers, collate_fn=collate_fn)
    test_loader = DataLoader(test_set, batch_size=batch_size, shuffle=False,
                              num_workers=num_workers, collate_fn=collate_fn)

    return train_loader, val_loader, test_loader
