"""
Dataset for loading pre-generated multi-type EQ data.
Returns (wet_audio, dry_audio, params_tensor) where params_tensor
includes gain, freq, q, and filter type index per band.
"""
import os
import json
import hashlib
import torch
import torchaudio
from torch.utils.data import Dataset, DataLoader
from pathlib import Path
from pipeline_utils import validate_band_list

FILTER_TYPES = ["peaking", "lowshelf", "highshelf", "highpass", "lowpass"]
TYPE_TO_INDEX = {t: i for i, t in enumerate(FILTER_TYPES)}


def _sha256_file(path: str, chunk_size: int = 1024 * 1024) -> str:
    digest = hashlib.sha256()
    with open(path, "rb") as f:
        while True:
            chunk = f.read(chunk_size)
            if not chunk:
                break
            digest.update(chunk)
    return digest.hexdigest()


class SyntheticEQDataset(Dataset):
    """
    Dataset to load synthetic wet/dry audio and multi-type target parameters
    generated by generate_data.py.
    """

    def __init__(
        self,
        data_dir,
        sample_rate=44100,
        duration=3.0,
        allow_missing_dry=False,
        verify_integrity=True,
        require_manifest_ok=True,
        strict_schema=True,
    ):
        self.data_dir = Path(data_dir)
        self.sample_rate = sample_rate
        self.samples = int(sample_rate * duration)
        self.allow_missing_dry = allow_missing_dry
        self.verify_integrity = verify_integrity
        self.require_manifest_ok = require_manifest_ok
        self.strict_schema = strict_schema
        self._warned_missing_dry = False
        self._group_key_cache = {}

        # Discover all target param files
        self.files = sorted(self.data_dir.glob("*_params.json"))
        self.manifest = self._load_manifest()
        self._verify_manifest_ready()

    def _load_manifest(self):
        manifest_path = self.data_dir / "manifest.json"
        if not manifest_path.exists():
            return None
        with open(manifest_path, "r") as f:
            return json.load(f)

    def _verify_manifest_ready(self):
        if self.manifest is None or not self.require_manifest_ok:
            return
        status = str(self.manifest.get("status", "ok")).lower()
        if status != "ok":
            raise RuntimeError(
                f"Dataset manifest status is '{status}', expected 'ok'. "
                "Refuse to load failed dataset artifacts."
            )

    def get_group_key(self, idx):
        if idx in self._group_key_cache:
            return self._group_key_cache[idx]
        param_path = self.files[idx]
        group_key = str(param_path.stem)
        try:
            with open(param_path, "r") as f:
                payload = json.load(f)
            if isinstance(payload, dict):
                group_key = str(
                    payload.get("source_relpath")
                    or payload.get("sample_id")
                    or group_key
                )
        except Exception:
            pass
        self._group_key_cache[idx] = group_key
        return group_key

    def __len__(self):
        return len(self.files)

    def __getitem__(self, idx):
        param_path = self.files[idx]
        file_stem = str(param_path).replace("_params.json", "")

        wet_path = f"{file_stem}_wet.wav"
        dry_path = f"{file_stem}_dry.wav"

        # Load audio
        if not Path(wet_path).exists():
            raise FileNotFoundError(f"Wet audio file not found: {wet_path}")
        wet_waveform, sr = torchaudio.load(wet_path)
        if sr != self.sample_rate:
            wet_waveform = torchaudio.functional.resample(wet_waveform, sr, self.sample_rate)

        missing_dry = False
        if not os.path.exists(dry_path):
            if not self.allow_missing_dry:
                raise FileNotFoundError(f"Missing dry waveform for sample {param_path.name}")
            missing_dry = True
            print(f"  [dataset] WARNING: dry file missing for {Path(wet_path).name}, using wet audio as dry (identity pair)")
            if not self._warned_missing_dry:
                self._warned_missing_dry = True
            dry_waveform = wet_waveform.clone()
        else:
            dry_waveform, dry_sr = torchaudio.load(dry_path)
            if dry_sr != self.sample_rate:
                dry_waveform = torchaudio.functional.resample(
                    dry_waveform,
                    dry_sr,
                    self.sample_rate,
                )
            if dry_waveform.shape[0] > 1:
                dry_waveform = torch.mean(dry_waveform, dim=0, keepdim=True)

        # Ensure single channel and exact length
        if wet_waveform.size(0) > 1:
            wet_waveform = torch.mean(wet_waveform, dim=0, keepdim=True)

        if wet_waveform.size(1) > self.samples:
            wet_waveform = wet_waveform[:, :self.samples]
        elif wet_waveform.size(1) < self.samples:
            wet_waveform = torch.nn.functional.pad(wet_waveform, (0, self.samples - wet_waveform.size(1)))

        if dry_waveform.size(1) > self.samples:
            dry_waveform = dry_waveform[:, :self.samples]
        elif dry_waveform.size(1) < self.samples:
            dry_waveform = torch.nn.functional.pad(dry_waveform, (0, self.samples - dry_waveform.size(1)))

        # Load parameters
        with open(param_path, 'r') as f:
            payload = json.load(f)
        if isinstance(payload, dict):
            params = payload.get("bands")
            source_relpath = str(payload.get("source_relpath", param_path.stem))
            schema_version = payload.get("schema_version")
            checksums = payload.get("checksums") or {}
        else:
            params = payload
            source_relpath = param_path.stem
            schema_version = None
            checksums = {}

        if self.strict_schema and schema_version is None:
            raise ValueError(
                f"Missing schema_version in {param_path.name}; refusing legacy payload in strict mode"
            )

        if self.verify_integrity:
            expected_wet = checksums.get("wet_sha256")
            expected_dry = checksums.get("dry_sha256")
            expected_params = checksums.get("params_sha256")
            manifest_requires_checksums = bool(
                (self.manifest or {}).get("quality", {}).get("checksum_files", False)
            )
            if manifest_requires_checksums and not checksums:
                raise ValueError(
                    f"Manifest requires checksums but none found in {param_path.name}"
                )
            if expected_wet and _sha256_file(wet_path) != expected_wet:
                raise ValueError(f"Checksum mismatch for wet audio: {wet_path}")
            if expected_dry and not missing_dry and _sha256_file(dry_path) != expected_dry:
                raise ValueError(f"Checksum mismatch for dry audio: {dry_path}")
            if expected_params and _sha256_file(str(param_path)) != expected_params:
                raise ValueError(f"Checksum mismatch for params payload: {param_path}")

        params = validate_band_list(params)

        # Convert to tensor: [gain, freq, q, type_idx] per band
        target_list = []
        for band in params:
            ftype_idx = TYPE_TO_INDEX[band["type"]]
            target_list.append([
                band["gain"],
                band["freq"],
                band["q"],
                float(ftype_idx)
            ])

        target_tensor = torch.tensor(target_list, dtype=torch.float32)

        # Separate components
        gain = target_tensor[:, 0]
        freq = target_tensor[:, 1]
        q = target_tensor[:, 2]
        filter_type = target_tensor[:, 3].long()

        return {
            "wet_audio": wet_waveform.squeeze(0),
            "dry_audio": dry_waveform.squeeze(0),
            "gain": gain,
            "freq": freq,
            "q": q,
            "filter_type": filter_type,
            "missing_dry": missing_dry,
            "source_relpath": source_relpath,
        }


def collate_fn(batch):
    """Custom collate to handle variable-length audio with padding."""
    wet_list = [item["wet_audio"] for item in batch]
    dry_list = [item["dry_audio"] for item in batch]

    max_len = max(x.shape[0] for x in wet_list)

    def pad_audio(audio, target_len):
        if audio.shape[0] < target_len:
            audio = torch.nn.functional.pad(audio, (0, target_len - audio.shape[0]))
        return audio

    wet_padded = torch.stack([pad_audio(w, max_len) for w in wet_list])
    dry_padded = torch.stack([pad_audio(d, max_len) for d in dry_list])
    gains = torch.stack([item["gain"] for item in batch])
    freqs = torch.stack([item["freq"] for item in batch])
    qs = torch.stack([item["q"] for item in batch])
    types = torch.stack([item["filter_type"] for item in batch])
    missing_dry_mask = torch.tensor(
        [bool(item.get("missing_dry", False)) for item in batch],
        dtype=torch.bool,
    )

    return {
        "wet_audio": wet_padded,
        "dry_audio": dry_padded,
        "gain": gains,
        "freq": freqs,
        "q": qs,
        "filter_type": types,
        "missing_dry_mask": missing_dry_mask,
        "source_relpath": [str(item.get("source_relpath", "")) for item in batch],
    }


def _group_aware_split_indices(dataset, split, seed):
    groups = {}
    for idx in range(len(dataset)):
        key = dataset.get_group_key(idx)
        groups.setdefault(key, []).append(idx)

    group_keys = list(groups.keys())
    generator = torch.Generator().manual_seed(seed)
    permutation = torch.randperm(len(group_keys), generator=generator).tolist()
    group_keys = [group_keys[i] for i in permutation]

    target_train = int(len(dataset) * split[0])
    target_val = int(len(dataset) * split[1])

    train_idx = []
    val_idx = []
    test_idx = []

    for key in group_keys:
        candidate = groups[key]
        if len(train_idx) < target_train:
            train_idx.extend(candidate)
        elif len(val_idx) < target_val:
            val_idx.extend(candidate)
        else:
            test_idx.extend(candidate)

    return train_idx, val_idx, test_idx


def create_dataloaders(
    data_dir,
    batch_size=32,
    num_workers=4,
    split=(0.8, 0.1, 0.1),
    seed=42,
    group_split=True,
):
    """Create train/val/test dataloaders."""
    dataset = SyntheticEQDataset(data_dir)

    if group_split:
        train_idx, val_idx, test_idx = _group_aware_split_indices(dataset, split, seed)
        train_set = torch.utils.data.Subset(dataset, train_idx)
        val_set = torch.utils.data.Subset(dataset, val_idx)
        test_set = torch.utils.data.Subset(dataset, test_idx)
    else:
        train_len = int(len(dataset) * split[0])
        val_len = int(len(dataset) * split[1])
        test_len = len(dataset) - train_len - val_len
        train_set, val_set, test_set = torch.utils.data.random_split(
            dataset,
            [train_len, val_len, test_len],
            generator=torch.utils.data.Generator().manual_seed(seed),
        )

    train_loader = DataLoader(train_set, batch_size=batch_size, shuffle=True,
                               num_workers=num_workers, collate_fn=collate_fn)
    val_loader = DataLoader(val_set, batch_size=batch_size, shuffle=False,
                             num_workers=num_workers, collate_fn=collate_fn)
    test_loader = DataLoader(test_set, batch_size=batch_size, shuffle=False,
                              num_workers=num_workers, collate_fn=collate_fn)

    return train_loader, val_loader, test_loader
